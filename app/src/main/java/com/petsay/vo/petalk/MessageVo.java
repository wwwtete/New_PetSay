package com.petsay.vo.petalk;

public class MessageVo{
	private int C_R;
	private int F;
	private int fans;
	
	public int getC_R() {
		return C_R;
	}
	public void setC_R(int c_R) {
		C_R = c_R;
	}
	public int getF() {
		return F;
	}
	public void setF(int f) {
		F = f;
	}
	public int getFans() {
		return fans;
	}
	public void setFans(int fans) {
		this.fans = fans;
	}
	
}
